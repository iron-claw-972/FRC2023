package frc.robot.util;

/**
 * Where the robot starts on the field during auto.
 */
public enum AutoStartPosition {
    
    /**
     * Grid position closest to the game field barrier.
     */
    RIGHT,

    /**
     * Grid position in the center of the grid.
     */
    CENTER,

    /**
     * Grid position closest to the barrier between the opponent's loading zone.
     */
    LEFT

}
